package viewmodel;

public class TimeTable {
	
	public String days;
	public String startTimes;	
	public String endTimes;	
	
	public TimeTable(){}

	public TimeTable(String days, String startTimes, String endTimes){
		this.days = days;
		this.startTimes = startTimes;
		this.endTimes = endTimes;
	}
	
}
